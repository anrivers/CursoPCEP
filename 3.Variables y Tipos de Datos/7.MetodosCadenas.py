texto = "   Hola, mundo!   "

#Convierte todo a minuscula:
print(texto.lower())  

#Convierte todo a mayuscula:
print(texto.upper())

#Va a sacar la medida de la cadena:
print(len(texto))

#Eliminar algo en especifico o espacios
print(texto.strip())

#Va a tomar un caracter y va a dividir la cadena en listas
print(texto.split("o"))