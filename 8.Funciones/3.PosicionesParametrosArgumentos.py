def saludar(nombre, mensaje):
    print(f"{mensaje}, {nombre}")
    
saludar("Andres","Hola")

def saludo(nombre, mensaje):
    print(f"Tu nombre es {nombre} y el mensaje que transmites es: {mensaje}")
    
saludo(mensaje="Bievenidos al himalayaaaa", nombre="Andres Rivera")

def saludamos(nombre, mensaje, fecha):
    print(f"Hola {nombre}, hoy es: {fecha} y tu mensaje es: {mensaje}")
    
saludamos("Andres Rivera", fecha="03/10/2024", mensaje="Bienvenido")