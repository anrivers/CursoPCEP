"""
Ejercicio básico:
Dadas las siguientes variables:
x = 25
y = 15
Utiliza operadores de comparación para comparar x e y, e imprime si:

a) x es igual a y

b) x es diferente de y

c) x es mayor que y

d) x es menor que y

e) x es mayor o igual que y

f) x es menor o igual que y
"""
x = 25
y = 15

print("x es igual a y:",x == y)
print("x es diferente de y:",x != y)
print("x es mayor que y:",x > y)
print("x es menor que y:",x < y)
print("x es mayor o igual que y:",x >= y)
print("x es menor o igual que y:",x <= y)
